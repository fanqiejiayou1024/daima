# 初始化字典
D = {"张三": 88, "李四": 90, "王五": 73, "赵六": 82}

# 1. 向字典中添加键值对“钱七”: 90
D["钱七"] = 90
print("添加键值对后:", D)

# 2. 修改“王五”对应的值为93
D["王五"] = 93
print("修改“王五”对应的值后:", D)

# 3. 删除“赵六”对应的键值对
del D["赵六"]
print("删除“赵六”对应的键值对后:", D)

# 4. 计算D的长度
length = len(D)
print("字典的长度:", length)

# 5. 清空D
D.clear()
print("清空字典后:", D)
